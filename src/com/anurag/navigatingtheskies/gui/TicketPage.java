    private void cancelBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cancelBtnMouseClicked
        // TODO add your handling code here:
        TicketDAO ticketDAO = new TicketDAO();
        boolean success = false;
        int choice  = JOptionPane.showConfirmDialog(jPanel1,"Are you sure, you want to cancel your ticket?","Confirmation",JOptionPane.YES_NO_OPTION);
        System.out.println(choice);
        System.out.println(JOptionPane.YES_OPTION);
        if(choice==0){
            System.out.println("Inside IF");
            try {
                success = ticketDAO.deleteTicket(this.ticketId, this.flightId);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"There was an error in deleting your ticket","Error",JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(TicketPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(success){
                JOptionPane.showMessageDialog(jPanel1,"Your ticket is cancelled","Info",JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }   
        }
    }//GEN-LAST:event_cancelBtnMouseClicked

