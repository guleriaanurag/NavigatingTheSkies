    private void fillTicketDetails(){
        TicketDAO ticketDAO = new TicketDAO();
        ExtendedTicket et = new ExtendedTicket();
        try {
            System.out.println(this.ticketId+" "+this.flightId);
            et = ticketDAO.fetchTicketDetails(this.ticketId, this.flightId);
        } catch (SQLException ex) {
            Logger.getLogger(TicketPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        flightSourceHeading.setText(et.getSource());
        flightSourceLabel.setText(et.getSource());
        flightDestinationHeading.setText(et.getDestination());
        flightDestinationLabel.setText(et.getDestination());
        airlineNameLabel.setText(et.getAirlinesName());
        flightIdLabel.setText(et.getFlightId());
        passengerNameLabel.setText(et.getPassengerName().toUpperCase());
        flightDepartureDateLabel.setText(et.getDepartureDate().toString());
        flightDepartureTimeLabel.setText(et.getDepartureTime().toString().concat(" hrs"));
        flightArrivalDateLabel.setText(et.getArrivalDate().toString());
        flightArrivalTimeLabel.setText(et.getArrivalTime().toString().concat(" hrs"));
        seatsBookedLabel.setText(String.valueOf(et.getSeatsBooked()));
        amountLabel.setText("INR ".concat(String.valueOf(et.getAmount())));
    }
    
